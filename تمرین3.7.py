# خط 1: تابعی به نامRecurs  تعریف شده است
def Recurs(n, s):
    # خط 2: شرطی که بررسی می‌کند آیا n کوچکتر از صفر است
    if n < 0:
        # خط 3: در صورت برقرار بودن شرط، "پایان" چاپ می‌شود
        print("پایان")
    else:
        # خط 4: در غیر اینصورت، مقادیر n و s چاپ می‌شوند
        print(n)
        print(s)
        # خط 5: تابع Recurs با ورودی n-1 و s فراخوانی می‌شود
        Recurs(n-1, s)